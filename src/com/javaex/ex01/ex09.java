package com.javaex.ex01;

public class ex09 {

	public static void main(String[] args) {
		// 관계 연산자
		
		int n1=5;
		int n2=3;
		
		System.out.println(n1<n2);
		System.out.println(n1>n2);
		System.out.println(n1<=n2);
		System.out.println(n1>=n2);
		System.out.println(n1==n2);
		System.out.println(n1==5);
		System.out.println(n2==3);
		System.out.println(n1!=n2);
	}

}
